public class Operasi {
    private int nilai;
    public Operasi (int nilai){
        this.nilai = nilai;
    }
    public int tambah(int a, int b) {
        return a + b;
    }
    public int kurang(int a, int b) {
        return a - b;
    }
    public int kali (int a, int b) {
        return a* b;
    }
    public float bagi (int a, int b) {
        return a / b;
    }
}
